#include <stdio.h>
/**
 * main - prints asdasdasd asdasdasd asdasd
 *
 * Return: int
 */
int main(void)
{
char alphabetmin, alphabetmay;
for (alphabetmin = 'a' ; alphabetmin <= 'z' ; alphabetmin++)
{
putchar(alphabetmin);
}
for (alphabetmay = 'A' ; alphabetmay <= 'Z' ; alphabetmay++)
{
putchar (alphabetmay);
}
putchar ('\n');
return (0);
}
